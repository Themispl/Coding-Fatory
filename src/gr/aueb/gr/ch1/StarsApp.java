package gr.aueb.gr.ch1;

public class StarsApp {
    public static void main(String[] args) {
        int star = 1;
        int i = 10;

        while (star <=10){
            System.out.print("*");
            System.out.println("*");
            star++;
        }
        while (i>=1){
            System.out.println("*");
            i--;
        }
    }
}
